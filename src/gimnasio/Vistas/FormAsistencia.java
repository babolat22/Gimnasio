    private void limpiarTabla(){
        DefaultTableModel model = (DefaultTableModel) jtListadoAsistencia.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
    
